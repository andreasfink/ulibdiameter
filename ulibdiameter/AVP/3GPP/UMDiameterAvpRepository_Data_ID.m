#import <ulibdiameter/UMDiameterAvp.h>
#import <ulibdiameter/UMDiameterAvpRepository_Data_ID.h>
#import <ulibdiameter/UMDiameterAvpService_Indication.h>
#import <ulibdiameter/UMDiameterAvpSequence_Number.h>
#import <ulibdiameter/UMDiameterAvpAVP.h>

@implementation UMDiameterAvpRepository_Data_ID


- (NSString *)avpType
{
    return @"Repository-Data-ID";
}

- (uint32_t)avpCode
{
    return 715;
}

+ (uint32_t)avpCode
{
    return 715;
}

- (void)genericInitialisation
{
    [super genericInitialisation];
    _avpFlags = UMDiameterAvpFlag_Vendor;
    _avpVendorId = 10415;
}

- (void)beforeEncode
{
    [super beforeEncode];

    NSMutableArray<UMDiameterAvp *> *arr = [[NSMutableArray alloc]init];
    if(_var_service_indication)
    {
        [_var_service_indication beforeEncode];
        [arr addObject:_var_service_indication];
    }
    if(_var_sequence_number)
    {
        [_var_sequence_number beforeEncode];
        [arr addObject:_var_sequence_number];
    }
    if(_var_avp.count > 0)
    {
        for(UMDiameterAvpAVP *o in _var_avp)
        {
            [o beforeEncode];
            [arr addObject:o];
        }
    }
    [self setArray:arr];
}


- (void)afterDecode
{
    [super afterDecode];
    NSArray *avps = [self array];

    NSMutableArray *knownAVPs  = [[NSMutableArray alloc]init];
    NSMutableArray *unknownAVPs;

    for(UMDiameterAvp *avp in avps)
    {
        if(avp.avpCode == [UMDiameterAvpService_Indication  avpCode])
        {
            _var_service_indication = [[UMDiameterAvpService_Indication alloc]initWithAvp:avp];
            [knownAVPs addObject:_var_service_indication];
        }
        else if(avp.avpCode == [UMDiameterAvpSequence_Number avpCode])
        {
            _var_sequence_number = [[UMDiameterAvpSequence_Number alloc]initWithAvp:avp];
            [knownAVPs addObject:_var_sequence_number];
        }
        else
        {
             if(unknownAVPs==NULL)
             {
                 unknownAVPs = [[NSMutableArray alloc]init];
             }
             [unknownAVPs addObject:avp];
        }
    }
    if(unknownAVPs.count>0)
    {
        _var_avp = unknownAVPs;
        [knownAVPs addObject:[_var_avp copy]];
    }
    [self setArray:knownAVPs];
}

+ (void)appendWebDiameterParameters:(NSMutableString *)s webName:(NSString *)webName  comment:(NSString *)webComment css:(NSString *)cssClass
{
    [s appendString:@"<tr>\n"];
    [s appendFormat:@"<td class=\"optional\">%@\n",webName];
    [s appendString:@"</td>\n"];
    [s appendString:@"<td>\n"];
    [s appendString:@"<table class=\"avp-grouped\">\n"];
	{
        NSString *webName2 = [NSString stringWithFormat:@"%@[service-indication]",webName];
        [UMDiameterAvpService_Indication appendWebDiameterParameters:s webName:webName2 comment:NULL css:@"mandatory"];
    }
	{
        NSString *webName2 = [NSString stringWithFormat:@"%@[sequence-number]",webName];
        [UMDiameterAvpSequence_Number appendWebDiameterParameters:s webName:webName2 comment:NULL css:@"mandatory"];
    }
    [s appendString:@"</table>\n"];
    [s appendString:@"</td>\n"];
    [s appendString:@"</tr>\n"];
}

- (id)objectValue
{
	UMSynchronizedSortedDictionary *dict = [[UMSynchronizedSortedDictionary alloc]init];
	dict[@"Service-Indication"] = [_var_service_indication objectValue];
	dict[@"Sequence-Number"] = [_var_sequence_number objectValue];
	return dict;
}

+ (id)definition
{
    UMSynchronizedSortedDictionary *avpDef = [[UMSynchronizedSortedDictionary alloc]init];
    avpDef[@"name"] = @"repository-data-id";
    avpDef[@"type"] = @"Grouped";
    avpDef[@"mandatory"] = @(NO);
    avpDef[@"vendor"] = @(YES);
    avpDef[@"group"] = @(YES);
    NSMutableArray *entries = [[NSMutableArray alloc]init];
    {
        UMSynchronizedSortedDictionary *def =  [UMDiameterAvpService_Indication definition];
        def[@"multiple"]=@(NO);
        def[@"mandatory"]=@(YES);
        [entries addObject:def];
    }
    {
        UMSynchronizedSortedDictionary *def =  [UMDiameterAvpSequence_Number definition];
        def[@"multiple"]=@(NO);
        def[@"mandatory"]=@(YES);
        [entries addObject:def];
    }
    avpDef[@"members"] = entries;

    return avpDef;
}


@end

